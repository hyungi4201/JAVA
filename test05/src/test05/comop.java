package test05;

public class comop {
	public static void main(String[] args) {
		int x;
		int y;
		x = 6;
		y = 9;
		System.out.println(x==y);//x와 y가 같은 값인지를 출력 : false
		System.out.println(x!=y);//x와 y가 다른 값인지를 출력 : true
		System.out.println(x>y);//x가 y보다 큰 값인지를 출력 : false
		System.out.println(x<y);//x가 y보다 작은 값인지를 출력 : true
		System.out.println(x>=y);//x가 y이상의 값인지를 출력 : false
		System.out.println(x<=y);//x가 y이하의 값인지를 출력 : true 
		
	}
}
